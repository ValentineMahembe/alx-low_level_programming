#include "3-calc.h"

/**
 * op_add - add 2 integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract 2 integer b from a
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get the remainder of integer division
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder from division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
